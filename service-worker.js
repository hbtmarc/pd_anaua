const CACHE_VERSION = 'anaua-checklist-no-cache-v34';

self.addEventListener('install', event => {
  self.skipWaiting();
});

self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys()
      .then(keys => Promise.all(keys.map(key => caches.delete(key))))
      .then(() => self.clients.claim())
  );
});

// No fetch caching. This file only neutralizes older service workers.
